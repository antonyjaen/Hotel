    private void btnguaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguaActionPerformed
        String nom,num,desc,med,precio,can;
        num=txtnumero.getText();
        desc=txtdesc.getText();
        med=cmbmedida.getSelectedItem().toString();
        nom=txtnom.getText();
        precio=txtprecio.getText();
        can=compra.getText();
        if(can.equals("")){
            JOptionPane.showMessageDialog(null, "Dato de 'Cantidad' incorrecto");
        }else{
            if(Integer.parseInt(can)==0){
                JOptionPane.showMessageDialog(null, "Dato de 'Cantidad' incorrecto");
            }else{
                Max2 ok = new Max2();
                int gasto=(Integer.parseInt(precio)*Integer.parseInt(can));
                try {
                    ok.carrito(iduser,nom,precio,can,num,gasto);
                    JOptionPane.showMessageDialog(null, "Agregado a la Cuenta");
                } catch (SQLException ex) {
                    Logger.getLogger(Comprar.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

        escribir();
        desh();

    }//GEN-LAST:event_btnguaActionPerformed

