    private void tablahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablahMouseClicked
        // TODO add your handling code here:

        int num;

        String nom,numero,desc,precio,cmb;
        num=tablah.rowAtPoint(evt.getPoint());
        // System.out.print(num=tablah.rowAtPoint(evt.getPoint()));

        nom = tablah.getModel().getValueAt(num, 4).toString();
        numero = tablah.getModel().getValueAt(num, 0).toString();
        desc = tablah.getModel().getValueAt(num, 1).toString();
        cmb = tablah.getModel().getValueAt(num, 2).toString();
        precio = tablah.getModel().getValueAt(num, 3).toString();
        // Object var=tablah.getColumnModel().getColumn(0);
        txtnom.setText(nom);
        txtnumero.setText(numero);
        txtdesc.setText(desc);
        txtprecio.setText(precio);
        cmbmedida.setSelectedItem(cmb);
        compra.setEditable(true);
        compra.setEnabled(true);
        hab();
    }//GEN-LAST:event_tablahMouseClicked
