        des=tablah.getModel().getValueAt(num, 2).toString();
        car=tablah.getModel().getValueAt(num, 3).toString();
        precio=tablah.getModel().getValueAt(num, 4).toString();
        estado=tablah.getModel().getValueAt(num, 5).toString();
