    private void btnguaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguaActionPerformed
        // TODO add your handling code here:
        Max2 ok = new Max2();
        String num,piso,des,car,precio,estado,tipo;

        num=txtnum.getText();
        des=txtdes.getText();
        car=txtcar.getText();
        precio=txtprecio.getText();
        piso=cmbpiso.getSelectedItem().toString();
        estado=cmbestado.getSelectedItem().toString();
        tipo=cmbtipo.getSelectedItem().toString();
        //ok.actualizarH(num,"Ocupado");
        try {
           if(ok.comprobarC(txtid.getText()).equals("null") || estado.equals("Ocupado") || estado.equals("Mantenimiento") || Integer.parseInt(txtdias.getText())==0){
           JOptionPane.showMessageDialog(null, "Error en los datos");
 
           }else{
           int total=Integer.parseInt(txtdias.getText())*Integer.parseInt(precio);
          if(ok.reserva(txtid.getText(),num,precio,txtdias.getText(),total).equals("")){
           ok.actualizarH(num,"Ocupado");
          } 
          
           escribir();
           desh();
           }
        } catch (SQLException ex) {
            Logger.getLogger(reserva.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnguaActionPerformed

